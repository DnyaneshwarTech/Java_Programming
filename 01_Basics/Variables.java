class Variables
{
    public static void main(String A[])
    {
        // Variable Declaration

        char grade;
        int age;
        float marks;
        double salary;

        // Variable Initialization

        grade = 'A';
        age = 25;
        marks = 99.89f;
        salary = 78.67890;

        // Print Values

        System.out.println("Grade : " + grade);
        System.out.println("Age : " + age);
        System.out.println("Marks : " + marks);
        System.out.println("Salary : " + salary);
    }
}